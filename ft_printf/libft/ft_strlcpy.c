#include "libft.h"

size_t	ft_strlcpy(char *dest, char *src, size_t size)
{
	size_t	i;
	size_t	a;

	a = 0;
	if (!dest || !src)
		return (a);
	i = 0;
	a = ft_strlen(src);
	if (size == 0)
		return (a);
	else
	{
		while (i < (size - 1) && src[i] != '\0')
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (a);
}
